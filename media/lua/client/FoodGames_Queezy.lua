FoodGames = FoodGames or {}
-----------------------            ---------------------------

function FoodGames.setQueezy(bool)
    local pl = getPlayer()
    local stats = pl:getStats()
    local bd = pl:getBodyDamage()
    if bool then
        if stats:getSickness() ~= 1 then
            stats:setSickness(1)
        end

        if bd:getFoodSicknessLevel() ~= 100 then
            bd:setFoodSicknessLevel(100)
        end
    end

end

function FoodGames.removeQueezy()
    local pl = getPlayer()
    local stats = pl:getStats()
    local bd = pl:getBodyDamage()

    if stats:getSickness() > 0 then
        stats:setSickness(0)
    end

    if bd:getFoodSicknessLevel() > 0 then
        bd:setFoodSicknessLevel(0)
    end
end

function FoodGames.updateQueezyHr()
    local pl = getPlayer()
    local curTime = getGameTime():getWorldAgeHours()
    local targTime = 24
    local md = pl:getModData()
    if not md['QueezyHr'] then return end
    md['QueezyHr'] = md['QueezyHr']  - 1
    if md['QueezyHr'] == 0 then
        md['QueezyHr'] = nil
        FoodGames.removeQueezy()
    end
    if not md['QueezyHr'] then
        FoodGames.removeQueezy()
    end
end

Events.EveryHours.Add(FoodGames.updateQueezyHr)

function FoodGames.Dizzy(pl)
    local md = pl:getModData()
    if not md then return end
    if not md['QueezyHr'] then return end
    FoodGames.setQueezy(true)
end
Events.OnPlayerUpdate.Add(FoodGames.Dizzy)
